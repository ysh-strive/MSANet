class Conv3(nn.Module):
    # Standard convolution
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super(Conv3, self).__init__()
        # print(c1, c2, k, s,)
        self.conv = nn.Conv2d(c1, c2, 3, s, 1, groups=g, bias=False)
        self.bn = nn.BatchNorm2d(c2)
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())
        self.channel = c1//2
        self.conv1 = nn.Sequential(
            nn.Conv2d(c1, self.channel, 1, s, autopad(k, p), groups=g, bias=False),  # 1x1
            # nn.Conv2d(c1, self.channel, 3, s, 1, groups=g, bias=False),  # 3x3
            nn.BatchNorm2d(self.channel),
            nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())
        )
    def forward(self, x):
        # print("Conv", x.shape)
        x = self.act(self.bn(self.conv(x)))
        return self.conv1(x)

    def fuseforward(self, x):
        return self.conv1(self.act(self.conv(x)))

